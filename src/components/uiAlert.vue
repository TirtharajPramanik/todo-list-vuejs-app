<template>
	<div
		class="flex p-4 mb-4 text-sm rounded-lg"
		:class="alertColor"
		role="alert"
	>
		<svg
			aria-hidden="true"
			class="inline flex-shrink-0 mr-3 w-5 h-5"
			fill="currentColor"
			viewBox="0 0 20 20"
			xmlns="http://www.w3.org/2000/svg"
		>
			<path
				fill-rule="evenodd"
				d="M18 10a8 8 0 11-16 0 8 8 0 0116 0zm-7-4a1 1 0 11-2 0 1 1 0 012 0zM9 9a1 1 0 000 2v3a1 1 0 001 1h1a1 1 0 100-2v-3a1 1 0 00-1-1H9z"
				clip-rule="evenodd"
			></path>
		</svg>
		<span class="sr-only">Info</span>
		<div v-if="state == 'success'">
			<span class="font-medium">Success !&nbsp;</span>
			<slot />
		</div>
		<div v-else-if="state == 'error'">
			<span class="font-medium">Error !&nbsp;</span>
			<slot />
		</div>
	</div>
</template>

<script lang="ts">
import { defineComponent } from 'vue';

export default defineComponent({
	props: {
		state: { type: String, reauired: false, default: 'success' },
	},
	computed: {
		alertColor() {
			if (this.state == 'success') return 'green';
			else if (this.state == 'error') return 'red';
		},
	},
});
</script>

<style scoped lang="css">
.green {
	@apply text-green-700 bg-green-100  dark:bg-green-200 dark:text-green-800;
}
.red {
	@apply text-red-700 bg-red-100 dark:bg-red-200 dark:text-red-800;
}
</style>
